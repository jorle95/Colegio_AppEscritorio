    private void actualizarListaP() {
            ArrayList<Profesor> profesores = ProfesorControlador.consultarRegistros();
        
        DefaultListModel modelo = new DefaultListModel();
        for (Profesor p : profesores) {
            //System.out.println(profesores.size());
            modelo.addElement("id: "+p.getId()+" - Nombre: "+p.getNombre());
        }
        
        this.jList1.setModel(modelo);
    }
    
