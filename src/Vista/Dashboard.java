    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // Seleccionar PROFESORES para editar
        String value = (String)this.jList1.getModel().getElementAt(this.jList1.locationToIndex(evt.getPoint()));
        String[] split = value.split(" ");
        String idString = split[1];
        int id = Integer.parseInt(idString);
        Profesor p = ProfesorControlador.obtenerRegistrosById(id);
        
        ModProf mp = new ModProf(p);
        mp.setVisible(true);
        mp.setLocationRelativeTo(null);
        this.setVisible(false);
    }//GEN-LAST:event_jList1MouseClicked

